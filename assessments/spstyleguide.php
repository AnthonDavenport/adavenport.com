        <h1>Style Guide</h1>
        <h3>1.	Color Scheme</h3>
        <h4>Background:</h4> 
        <p>Colors for graphic will use a big split Complimentary color scheme of the following colors:<br>
        Orange #FF8000,<br>
        Green #04B431,<br>
        Purple #8904B1,<br>
        Blue #29088A <br>
        The main background colors will be Monochromatic:<br>
        White #FFFFFF<br>
        Black #000000<br>
        Gray #6E6E6E</p><br>
        <h4>Text: It can be one of the following colors for a title or heading:</h4>
        <p>Orange #FF8000,<br>
        Green #04B431,<br>
        Purple #8904B1,<br>
        Blue #29088A <br>
         For body copy one of the following:<br>
        White #FFFFFF<br>
        Black #000000<br>
        Gray #6E6E6E</p>
        <h4>Links:</h4>
        <p>Unselected: One of the four main colors used in the background graphics, it just needs to be different than the title and header.<br>
        On Hover:<br>
        Match the color of the title or heading<br>
        On Click:<br>
        Give the link a different value of color, either darker or lighter.<br>
        On Selected:<br>
        Give the link a complementary color opposite the original color of the link.</p><br>
        <h4>Accents: will match to the surrounding font color.</h4>
        <h4>Highlights:</h4>
        <p>Highlighted areas will be a default blue color.</p>
        <h4>Logos: The logo will fit the color scheme of the graphic border.</h4>
        <h3>2.	Typography</h3>
        <p>Font: Title Copy – Arial Bold Sans Serif<br>
        Body copy – Adobe Garamond Pro serif<br>
        Size: <br>
        Headers<br>
        h1:  60 px<br>
        h2:  48 px<br>
        h3:  36 px<br>
        h4:  24 px<br>
        h5:  20 px<br>
        h6:  18 px<br>
        Body Copy<br>
        P: 14-16<br>
        Behavior:<br>  
        Color change on hover to contrast with the background<br>
        Color change on click<br>
        Color change on selected <br>
        Appearance:<br> 
        Hope to provide contrast and clarity to words with contrast and value.</p><br>
